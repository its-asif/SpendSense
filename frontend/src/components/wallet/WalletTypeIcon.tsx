function SmartphoneIcon({ className }: { className?: string }) {
  return (
    <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round" className={className}>
      <rect x="7" y="2" width="10" height="20" rx="2" />
      <path d="M11 18h2" />
    </svg>
  );
}

export function WalletTypeIcon({ walletType, className = '' }: WalletTypeIconProps) {
  const normalized = walletType.trim().toUpperCase();

  if (normalized === 'BANK') {
    return <BuildingIcon className={className} />;
  }

  if (normalized === 'CARD') {
    return <CardIcon className={className} />;
  }

  if (normalized === 'MOBILE_WALLET') {
    return <SmartphoneIcon className={className} />;
  }

  return <WalletIcon className={className} />;
}